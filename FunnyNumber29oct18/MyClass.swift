import Foundation

class MyClass{
    var myNumberInt: Int = 0
    
    func decreseNum() -> String {
        myNumberInt -= 1
        return String(myNumberInt)
    }
    
    func increase() -> String {
        myNumberInt += 1
        return String(myNumberInt)
    }
    
    
    
}   // MyClass


